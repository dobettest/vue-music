<template>
  <transition name="slide">
    <div class="user-center">
      <div class="back" @click="back">
        <i class="icon-back"></i>
      </div>
      <div class="switches-wrapper">
        <switches :switches="switches" :currentIndex="currentIndex" @switch="switchItem"></switches>
      </div>
      <div class="play-btn" ref="playBtn" @click="random">
        <i class="icon-play"></i>
        <span class="text">随机播放全部</span>
      </div>
      <div class="list-wrapper" ref="listWrapper">
        <div class="list-scroll" ref="listScroll" v-if="currentIndex === 0">
          <div class="list-inner">
            <song-list :songs="favoriteList" @select="selectSong"></song-list>
          </div>
        </div>
        <div class="list-scroll" ref="listScroll" v-if="currentIndex === 1">
          <div class="list-inner">
            <song-list :songs="playHistory" @select="selectSong"></song-list>
          </div>
        </div>
      </div>
      <div class="no-result-wrapper" v-show="noResult">
        <no-result :title="noResultTitle"></no-result>
      </div>
    </div>
  </transition>
</template>

<script>
import Switches from "base/switches/index";
import SongList from "base/song-list/index";
import BScroll from "@better-scroll/core";
import ScrollBar from "@better-scroll/scroll-bar";
import NoResult from "base/no-result/index";
import Song from "common/js/song";
BScroll.use(ScrollBar);
import { mapGetters, mapActions } from "vuex";
import { playlistMixin } from "common/js/mixin";

export default {
  mixins: [playlistMixin],
  data() {
    return {
      currentIndex: 0,
      switches: [{ name: "我喜欢的" }, { name: "最近听的" }],
      bscroll: null
    };
  },
  mounted() {
    // 初始化滑动组件
    this._initBscroll();
  },
  computed: {
    noResult() {
      if (this.currentIndex === 0) return !this.favoriteList.length;
      else return !this.playList.length;
    },
    noResultTitle() {
      if (this.currentIndex === 0) return "没有收藏歌曲";
      else return "没有听过的歌曲";
    },
    ...mapGetters(["favoriteList", "playHistory"])
  },
  methods: {
    back() {
      this.$router.back();
    },
    _initBscroll() {
      this.bscroll = new BScroll(this.$refs.listScroll, {
        scrollY: true,
        probeType: 1,
        scrollbar: true,
        click: true
      });
    },
    selectSong(song) {
      this.insertSong(new Song(song));
    },
    // 切换tag
    switchItem(index) {
      this.currentIndex = index;
      setTimeout(() => {
        this.bscroll.refresh();
      }, 20);
    },
    // 随机播放
    random() {
      let list = this.currentIndex === 0 ? this.favoriteList : this.playHistory;
      if (list.length === 0) return;
      list = list.map(song => {
        return new Song(song);
      });
      this.randomPlay({
        list
      });
    },
    handlePlayList(playList) {
      const bottom = playList.length > 0 ? "60px" : "";
      this.$refs.listWrapper.style.bottom = bottom;
      if (this.bscroll) this.bscroll.refresh();
    },
    ...mapActions(["insertSong", "randomPlay"])
  },
  watch: {
    playHistory() {
      setTimeout(() => {
        this.bscroll.refresh();
      }, 20);
    },
    favoriteList() {
      setTimeout(() => {
        this.bscroll.refresh();
      }, 20);
    }
  },
  components: {
    Switches,
    SongList,
    NoResult
  }
};
</script>

